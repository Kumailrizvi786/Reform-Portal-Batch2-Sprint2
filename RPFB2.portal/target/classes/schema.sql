CREATE TABLE ANGEL HELPER (
  a_Id(id) ID(255),
  Name VARCHAR(255) NOT NULL,
  Contact Number INTEGER(255) NOT NULL,
  Area _of_ Interest VARCHAR(255) NOT NULL,
  Address VARCHAR(255) NOT NULL,
  Pincode INTEGER(255) NOT NULL,
  Email VARCHAR(255) NOT NULL,
  PRIMARY KEY (a_Id(id))
);

CREATE TABLE ANGEL DETAILS (
  Type(individual or organization) VARCHAR(255) NOT NULL,
  Profession  TEXT(255),
  Qualification  VARCHAR(255),
  GST Number (id) ID(255) NOT NULL,
  Contact Person Name  INTEGER(255) NOT NULL,
  Status VARCHAR(255) ,
  Profile Picture  BLOB(30000),
  FiLeCreated On TIMESTAMP  DEFAULT CURRENT_TIMESTAMP ,
  Last Updated On TIMESTAMP DEFAULT CURRENT_TIMESTAMP ,
  a_Id(id) ID(255),
  PRIMARY KEY (GST Number (id)),
  FOREIGN KEY (a_Id(id)) REFERENCES ANGEL HELPER(a_Id(id))
);

CREATE TABLE LOG IN (
  Email VARCHAR(255) NOT NULL,
  Password VARCHAR(255) NOT NULL,
  Created On  TIMESTAMP  DEFAULT CURRENT_TIMESTAMP,
  Last Updated On  TIMESTAMP  DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (Email) REFERENCES ANGEL HELPER(Email)
);